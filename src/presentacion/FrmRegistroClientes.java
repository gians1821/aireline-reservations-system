    private Empleado obtenerEmpleadoPorNombre(String nombreEmpleado) {
        NodoEmpleado p = le.getInicio();
        while (p != null) {
            if (p.getInfo().getNombre().equals(nombreEmpleado)) {
                return p.getInfo();
            }
            p = p.getSgte();
        }
        return null;
    }
    
    
