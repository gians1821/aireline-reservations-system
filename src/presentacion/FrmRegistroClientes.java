    private void btnRegistrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarClienteActionPerformed
        try{
            String nombre = txtNombreCliente.getText();
            String dni = txtDniCliente.getText();
            String destino = (String) cbDestino.getSelectedItem();
            int nAsiento = Integer.parseInt(txtNAsiento.getText());
            
            try{
                int numero = Integer.parseInt(txtNAsiento.getText());
                if(!la.contiene(numero)){
                    laa.eliminarAsientoAbordo(numero);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Escoja otro asiento por favor");
                }
                int costo = costoPasaje;
                String id = txtID.getText();
                String nombreAvion = txtNombreAvion.getText();
                int maletas = Integer.parseInt(txtCantidadMaletas.getText());
                String nombreEmpleado = jComboBox1.getSelectedItem().toString();
                Empleado empleadoSeleccionado = obtenerEmpleadoPorNombre(nombreEmpleado);
                if(empleadoSeleccionado!=null){
                    Cliente cliente = new Cliente(nombre, dni, destino, nAsiento, costo, id, nombreAvion, maletas);
                    empleadoSeleccionado.registrarCliente(cliente);
                    lc.InsertarFinal(cliente);
                    JOptionPane.showMessageDialog(null, "Muchas Gracias por su preferencia. ¡Feliz Vuelo Sr/a " + nombre+"!");
                }
                else {
                    JOptionPane.showMessageDialog(null, "Seleccione un empleado válido");
                }
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Ingrese un valor válido");
            }
            
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Ingrese un valor válido");
        }
        txtNombreCliente.setText("");
        txtDniCliente.setText("");
        txtNAsiento.setText("");
        txtID.setText("");
        txtCantidadMaletas.setText("");
    }//GEN-LAST:event_btnRegistrarClienteActionPerformed

