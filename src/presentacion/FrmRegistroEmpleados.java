    private void btnRegistrarEmpleadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarEmpleadosActionPerformed
        try{
            String nombre = txtNombreEmpleado.getText();
            String dni= txtDniEmpleado.getText();
            empleado= new Empleado(nombre, dni);
            le.InsertarFinal(empleado);
            if(le.contar()>=1){
                btnRegistrarClientes.setEnabled(true);
            }
            else
                btnRegistrarClientes.setEnabled(false);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Ingrese un valor v√°lido");
        }
        txtDniEmpleado.setText("");
        txtNombreEmpleado.setText("");
        txtNombreEmpleado.requestFocus();
    }//GEN-LAST:event_btnRegistrarEmpleadosActionPerformed

